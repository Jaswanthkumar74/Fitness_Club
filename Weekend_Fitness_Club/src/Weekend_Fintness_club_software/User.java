package Weekend_Fintness_club_software;


public class User {
	String fullname;
	String mobileno;
	int age;
	String password;
	
	
	
	public User(String fullname, String mobileno, int age, String password) {
		super();
		this.fullname = fullname;
		this.mobileno = mobileno;
		this.age = age;
		this.password = password;
	}

	
}
